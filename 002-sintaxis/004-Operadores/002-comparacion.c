#include <stdio.h>

int main(int argc,char *argv[]){
    printf("mayor que: %i",(4>3));
    printf("menor que: %i",(4<3));
    
    printf("mayor o igual que: %i",(4>=3));
    printf("menor o igual que: %i",(4<=3));
    
    printf("igual que: %i",(4==3));
    printf("no es igual que: %i",(4!=3));
    
    return 0;
}

